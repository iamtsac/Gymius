package com.example.gymius;

public class GroupProgram {
    private int id,capacity;
    private String name;

    public void setId(int id){
        this.id = id;
    }
    public int getId(){
        return id;
    }
    public void setPrice(int price){
        this.capacity = price;
    }
    public int getPrice(){
        return capacity;
    }
    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return name;
    }


}
